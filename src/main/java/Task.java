public class Task {
    protected String description;
    protected boolean isDone;

    public Task(String description) {
        this.description = description;
        this.isDone = false;
    }

    public void mark() {
        this.isDone = true;
    }

    public void unmark() {
        this.isDone = false;
    }

    @Override
    public String toString() {
        char statusIcon = this.isDone ? 'X' : ' ';
        return "[" + statusIcon + "] " + this.description;
    }
}
