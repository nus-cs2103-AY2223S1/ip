class SkeletonDuke {

    SkeletonDuke() {
    }

    void greet() {
        System.out.println("Hello! I'm Duke");
        System.out.println("What can I do for you?");
    }

    void echo(String s) {
        System.out.println(s);
    }

    void exit() {
        System.out.println("Bye. Hope to see you again soon!");
    }

}